#!/bin/bash
set -e

# --------------------------------------------------------
# .env から設定を読み込む
# --------------------------------------------------------
if [ -f .env ]; then
    export $(grep -v '^\s*#' .env | grep -v '^\s*$' | xargs)
fi

VENV_PATH="${VENV_PATH:-./.venv}"
IN_DIR="in"
OUT_DIR="out"

echo "【設定】"
echo "  仮想環境: $VENV_PATH"
echo "  入力フォルダ: $IN_DIR"
echo "  出力フォルダ: $OUT_DIR"
echo ""

# --------------------------------------------------------
# 入力フォルダの確認
# --------------------------------------------------------
if [ ! -d "$IN_DIR" ]; then
    echo "[エラー] 入力フォルダ '$IN_DIR' が存在しません。"
    exit 1
fi

CSV_COUNT=$(ls "$IN_DIR"/*.csv 2>/dev/null | wc -l | tr -d ' ')
if [ "$CSV_COUNT" -eq 0 ]; then
    echo "[エラー] '$IN_DIR' フォルダにCSVファイルが存在しません。"
    exit 1
fi
echo "入力CSVファイル数: $CSV_COUNT 件"

# --------------------------------------------------------
# 出力フォルダの作成
# --------------------------------------------------------
mkdir -p "$OUT_DIR"

# --------------------------------------------------------
# 仮想環境の作成（初回のみ）
# --------------------------------------------------------
if [ ! -d "$VENV_PATH" ]; then
    echo "仮想環境を作成しています: $VENV_PATH"
    python3 -m venv "$VENV_PATH"
fi

# --------------------------------------------------------
# 仮想環境の有効化
# --------------------------------------------------------
. "$VENV_PATH/bin/activate"
echo "仮想環境を有効化しました: $(python --version)"

# --------------------------------------------------------
# 依存パッケージのインストール
# --------------------------------------------------------
echo "依存パッケージをインストールしています..."
pip install -r requirements.txt -q

# --------------------------------------------------------
# 実行
# --------------------------------------------------------
echo ""
echo "異常検知を開始します..."
python main.py

echo ""
echo "完了。結果は '$OUT_DIR' フォルダを確認してください。"
